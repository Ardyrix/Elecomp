public class ListHutangBG {

    private int nomor, biaya, kode_supplier;
    private String no_fakturBG, tanggal, namaSupplier, kotaSupplier, noSeri;

    public int getKode_supplier() {
        return kode_supplier;
    }

    public void setKode_supplier(int kode_supplier) {
        this.kode_supplier = kode_supplier;
    }

    public int getNomor() {
        return nomor;
    }

    public void setNomor(int nomor) {
        this.nomor = nomor;
    }

    public int getBiaya() {
        return biaya;
    }

    public void setBiaya(int biaya) {
        this.biaya = biaya;
    }

    public String getNo_fakturBG() {
        return no_fakturBG;
    }

    public void setNo_fakturBG(String no_fakturBG) {
        this.no_fakturBG = no_fakturBG;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getNoSeri() {
        return noSeri;
    }

    public void setNoSeri(String noSeri) {
        this.noSeri = noSeri;
    }

    public String getNamaSupplier() {
        return namaSupplier;
    }

    public void setNamaSupplier(String namaSupplier) {
        this.namaSupplier = namaSupplier;
    }

    public String getKotaSupplier() {
        return kotaSupplier;
    }

    public void setKotaSupplier(String kotaSupplier) {
        this.kotaSupplier = kotaSupplier;
    }
}
